package com.example.fernana6.kotlin.domain.commands

import com.example.fernana6.kotlin.data.ForecastRequest
import com.example.fernana6.kotlin.domain.mappers.ForecastDataMapper
import com.example.fernana6.kotlin.domain.model.DomainClasses
import com.example.fernana6.kotlin.domain.model.ForecastList

class RequestForecastCommand(private val zipCode: String) : Command<ForecastList> {
    override fun execute(): ForecastList {
        val forecastRequest = ForecastRequest(zipCode)
        return ForecastDataMapper().convertFromDataModel(forecastRequest.execute())
    }
}